import assert from 'node:assert/strict';
import fs from 'node:fs';
import { execFileSync } from 'node:child_process';

const output = execFileSync(process.execPath, ['scripts/identity-verify-drift.mjs'], { encoding: 'utf8' });
assert.match(output, /Identity Verify Drift/, 'CLI title is printed');
assert.match(output, /Status: VERIFY_DRIFT_ANALYZED/, 'drift is analyzed');
assert.match(output, /Safety: NOT SAFE - Repeat Verify Required/, 'baseline is not marked safe without old payload');

const reportPath = 'backups/identity/verify-drift/verify-drift-report.json';
const redactedPath = 'backups/identity/verify-drift/verify-drift-report-redacted.json';
const mdPath = 'backups/identity/verify-drift/verify-drift-report.md';
const redactedMdPath = 'backups/identity/verify-drift/verify-drift-report-redacted.md';
for (const file of [reportPath, redactedPath, mdPath, redactedMdPath]) {
  assert.equal(fs.existsSync(file), true, `${file} exists`);
}

const report = JSON.parse(fs.readFileSync(reportPath, 'utf8'));
assert.equal(report.status, 'VERIFY_DRIFT_ANALYZED', 'report status is forensic-only');
assert.equal(report.hashes.expectedVerify, 'ff3a9ce86a9d', 'expected verify hash is preserved');
assert.notEqual(report.hashes.currentVerify, report.hashes.expectedVerify, 'current verify hash mismatch is detected');
assert.equal(report.business.manifest.unchanged, true, 'manifest is unchanged');
assert.equal(report.business.dryRun.unchanged, true, 'dry run is unchanged');
assert.equal(report.business.invariants.blockingCount, 0, 'blocking invariants remain zero');
assert.equal(report.oldVerifyArtifact.available, false, 'old verify payload absence is explicit');
assert.equal(report.hashContribution.exactContributionAvailable, false, 'exact hash contribution is not invented');
assert.equal(report.safety.verifyBaselineUpdated, false, 'baseline not updated');
assert.equal(report.safety.verifyRegenerated, false, 'verify not regenerated by drift analysis');
assert.equal(report.safety.canaryStarted, false, 'canary not started');
assert.equal(report.safety.cutoverStarted, false, 'cutover not started');
assert.equal(report.safety.productionDeployed, false, 'production not deployed');
assert.ok(report.timeline.verifyEventsAfterCanary.length >= 1, 'audit proves verify ran after canary');
assert.ok(report.structuralDiff.some(item => item.field === 'verifyReport.generatedAt'), 'generatedAt drift is represented');
assert.ok(report.structuralDiff.some(item => item.field === 'manifest.hash'), 'manifest comparison is represented');
assert.ok(report.structuralDiff.some(item => item.field === 'dryRun.hash'), 'dry run comparison is represented');

const source = fs.readFileSync('scripts/identity-verify-drift.mjs', 'utf8');
assert.doesNotMatch(source, /appendReviewAudit|identity-v2-admin|cutover-postgres|rollback-postgres|import-identity|runRemote|deploy-frontend|setDoc|updateDoc|deleteDoc|collection\(|getFirestore|getAuth|firebase-admin/i, 'drift analysis contains no production mutation calls');

